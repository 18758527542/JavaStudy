package day03;

public class math {
    public static void main(String[] args) {
        double a = 4.24;
        double b = Math.sqrt(Math.abs(a));
        double c = Math.ceil(a);
        double d = Math.floor(a);
        double e = Math.round(a);
        System.out.println(a + " " + b + " " + c + " " + d + " " + e);
    }
}
